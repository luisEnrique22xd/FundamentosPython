#operadores booleanos
# > >=
print(10>10)
print(10>=10)
# < <=
print(10<10)
print(10<=10)
# ==
print(10==10)
# != 
print(10!=10)


#operadores logicos 
# and (True if all true)
print(True and True)
print(True and False)
# or (True if all least one true)
print(True or True)
print(True or False)
# not (not True )
print(not True)
print(not False)


